/* ===== MENU & SCROLL ===== */
$(document).ready(function(){
    $('#menu').click(function(){
        $(this).toggleClass('fa-times');
        $('.navbar').toggleClass('active');
    });

    // Dark mode toggle
    $('#theme-btn').click(function(){
        $(this).toggleClass('fa-sun');
        $('body').toggleClass('dark');
    });

    let top = $(window).scrollTop();
    if(top > 0) {
        $('.scroll-top').addClass('active');
    } else {
        $('.scroll-top').removeClass('active');
    }

    $(window).on('scroll load', function(){
        $('#menu').removeClass('fa-times');
        $('.navbar').removeClass('active');

        // Scroll Spy
        $('section').each(function(){
            let top = $(window).scrollTop();
            let height = $(this).height();
            let offset = $(this).offset().top - 200;
            let id = $(this).attr('id');

            if(top >= offset && top < offset + height){
                $('.navbar ul li a').removeClass('active');
                $('.navbar').find(`[href="#${id}"]`).addClass('active');
            }
        });
    });
});

/* ===== TYPED.JS ===== */
let typedInstance;
function initTyped(strings) {
    if(typedInstance) typedInstance.destroy();
    typedInstance = new Typed('.typing-text', {
        strings: strings,
        loop: true,
        typeSpeed: 50,
        backSpeed: 25,
        backDelay: 1500
    });
}
// Init par défaut en Français
initTyped(translations['fr'].typed_strings);

/* ===== TRANSLATION LOGIC ===== */
const btnFr = document.getElementById("btn-fr");
const btnEn = document.getElementById("btn-en");
const cvLink = document.getElementById("download-cv");

function switchLang(lang) {
    document.documentElement.lang = lang;
    document.title = translations[lang].doc_title;
    
    $("[data-i18n]").each(function() {
        const key = $(this).attr("data-i18n");
        if(translations[lang][key]) {
            $(this).text(translations[lang][key]);
        }
    });

    if(cvLink) cvLink.href = translations[lang].cv_link;
    
    $("[data-i18n-placeholder]").each(function() {
        const key = $(this).attr("data-i18n-placeholder");
        if(translations[lang][key]) $(this).attr("placeholder", translations[lang][key]);
    });
    $("[data-i18n-value]").each(function() {
        const key = $(this).attr("data-i18n-value");
        if(translations[lang][key]) $(this).val(translations[lang][key]);
    });

    if (lang === 'fr') {
        btnFr.classList.add('active');
        btnEn.classList.remove('active');
    } else {
        btnEn.classList.add('active');
        btnFr.classList.remove('active');
    }
    
    initTyped(translations[lang].typed_strings);
}
btnFr.addEventListener("click", () => switchLang('fr'));
btnEn.addEventListener("click", () => switchLang('en'));

